export default function Sidebar() {
  const pathname = usePathname();
  const [userName, setUserName] = useState('');
  const [userRole, setUserRole] = useState('');
  const [isLoggingOut, setIsLoggingOut] = useState(false);
  const { logout, user } = useFirestoreAuthContext();
  const [navigationItems, setNavigationItems] = useState<SidebarItem[]>([]);

  useEffect(() => {
    if (user) {
      // Firebase'den gelen kullanıcı bilgileri
      setUserName(user.displayName || 'Kullanıcı');
      
      // LocalStorage'dan rol bilgisi
      const role = safeGetItem('user_role') || 'user';
      setUserRole(role);
      
      // Role göre navigasyon ayarla
      if (role === 'admin') {
        setNavigationItems(adminNavigation);
      } else if (role === 'coreteam') {
        setNavigationItems(coreTeamNavigation);
      } else {
        setNavigationItems(userNavigation);
      }
    }
  }, [user]);

  // Çıkış yapma fonksiyonu
  const handleLogout = async () => {
    if (isLoggingOut) return; // Çift tıklamayı önle
    
    setIsLoggingOut(true);
    
    try {
      // Önce LocalStorage'daki token bilgilerini manuel olarak temizleyelim
      safeRemoveItem('auth_token');
      safeRemoveItem('user_name');
      safeRemoveItem('user_email');
      safeRemoveItem('user_role');
      safeRemoveItem('remember_me');
    
      // Firebase çıkış işlemini çalıştır
      const result = await logout();
      
      if (result.error) {
        console.error('Çıkış yapılırken hata:', result.error);
        // Hata olsa bile login sayfasına yönlendirelim
        window.location.href = '/login'; // router.push kullanmak yerine tam sayfa yenilemeyle yönlendir
      }
    } catch (error) {
      console.error('Çıkış işleminde beklenmeyen hata:', error);
      // Hata olsa bile login sayfasına yönlendir
      window.location.href = '/login';
    }
  };

  return (
    <div className="flex h-full flex-col border-r border-gray-200 dark:border-gray-800 bg-white dark:bg-gray-900">
      <div className="flex flex-col flex-grow pt-5 pb-4 overflow-y-auto">
        <div className="flex items-center flex-shrink-0 px-4 mb-5">
          <Link href="/dashboard" className="flex items-center">
            <span className="text-xl font-semibold text-blue-600">HSD Dashboard</span>
          </Link>
        </div>
        <div className="mt-5 flex flex-col flex-1">
          <nav className="flex-1 space-y-1 px-2" aria-label="Sidebar">
            {navigationItems.map((item) => {
              // Sadece tam eşleşme kontrolü yapıyoruz
              // Böylece "/dashboard/coreteam/tasks" sayfasındayken sadece "Görevler" menüsü aktif olacak
              const isActive = pathname === item.href;
              
              return (
                <Link
                  key={item.name}
                  href={item.href}
                  className={`${
                    isActive
                      ? 'bg-blue-50 dark:bg-blue-900/20 text-blue-600 dark:text-blue-400'
                      : 'text-gray-600 dark:text-gray-400 hover:bg-gray-50 dark:hover:bg-gray-800 hover:text-gray-900 dark:hover:text-gray-200'
                  } group flex items-center px-2 py-2 text-sm font-medium rounded-md transition-colors`}
                >
                  <item.icon
                    className={`${
                      isActive ? 'text-blue-600 dark:text-blue-400' : 'text-gray-400 dark:text-gray-500 group-hover:text-gray-500 dark:group-hover:text-gray-400'
                    } mr-3 flex-shrink-0 h-6 w-6 transition-colors`}
                  />
                  {item.name}
                </Link>
              );
            })}
          </nav>
        </div>
      </div>
      <div className="flex-shrink-0 border-t border-gray-200 dark:border-gray-800">
        {/* Kullanıcı profil bölümü */}
        <div className="px-4 py-4">
          <div className="flex items-center">
            <div className="flex-shrink-0">
              <div className="relative w-10 h-10 rounded-full bg-gradient-to-r from-blue-500 to-blue-600 flex items-center justify-center text-white font-medium text-lg shadow-md">
                {userName.charAt(0).toUpperCase()}
                <span className="absolute bottom-0 right-0 block h-2.5 w-2.5 rounded-full bg-green-400 border-2 border-white dark:border-gray-900"></span>
              </div>
            </div>
            <div className="ml-3">
              <p className="text-sm font-medium text-gray-800 dark:text-gray-200 truncate">{userName}</p>
              <div className="flex items-center">
                <span className={`inline-flex items-center rounded-full px-2 py-0.5 text-xs ${
                  userRole === 'admin' 
                    ? 'bg-purple-100 text-purple-800 dark:bg-purple-900/30 dark:text-purple-400' 
                    : userRole === 'coreteam' 
                      ? 'bg-teal-100 text-teal-800 dark:bg-teal-900/30 dark:text-teal-400'
                      : 'bg-blue-100 text-blue-800 dark:bg-blue-900/30 dark:text-blue-400'
                }`}>
                  {userRole === 'admin' ? 'Yönetici' : userRole === 'coreteam' ? 'CoreTeam' : 'Kullanıcı'}
                </span>
              </div>
            </div>
          </div>
        </div>
        
        {/* Çıkış yapma ve ana sayfa butonları */}
        <div className="px-2 pb-2 pt-1 space-y-1">
          <button
            onClick={handleLogout}
            disabled={isLoggingOut}
            className={`w-full text-left flex items-center px-2 py-2 text-sm font-medium rounded-md text-red-600 dark:text-red-400 hover:bg-red-50 dark:hover:bg-red-900/20 transition-colors ${isLoggingOut ? 'opacity-70 cursor-not-allowed' : ''}`}
          >
            {isLoggingOut ? (
              <svg className="animate-spin mr-3 flex-shrink-0 h-5 w-5" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
                <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4"></circle>
                <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
              </svg>
            ) : (
            <svg 
              xmlns="http://www.w3.org/2000/svg" 
              className="mr-3 flex-shrink-0 h-5 w-5" 
              fill="none" 
              viewBox="0 0 24 24" 
              stroke="currentColor"
            >
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
            </svg>
            )}
            {isLoggingOut ? 'Çıkış Yapılıyor...' : 'Çıkış Yap'}
          </button>
          
          <Link
            href="/"
            className="w-full flex items-center px-2 py-2 text-sm font-medium rounded-md text-gray-600 dark:text-gray-400 hover:bg-gray-50 dark:hover:bg-gray-800 hover:text-gray-900 dark:hover:text-gray-200 transition-colors"
          >
            <svg 
              xmlns="http://www.w3.org/2000/svg" 
              className="mr-3 flex-shrink-0 h-5 w-5" 
              fill="none" 
              viewBox="0 0 24 24" 
              stroke="currentColor"
            >
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M3 12l2-2m0 0l7-7 7 7M5 10v10a1 1 0 001 1h3m10-11l2 2m-2-2v10a1 1 0 01-1 1h-3m-6 0a1 1 0 001-1v-4a1 1 0 011-1h2a1 1 0 011 1v4a1 1 0 001 1m-6 0h6" />
            </svg>
            Ana Sayfa
          </Link>
        </div>
      </div>
    </div>
  );
} 